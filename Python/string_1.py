my_str = 'Hello World!'
print(my_str)       # Prints the complete string
print(my_str[0])    # Prints the first character of the string
print(my_str[2:5])  # Prints characters starting from the 3rd to 5th
print(my_str[2:])   # Prints the string starting from the 3rd character
print(my_str * 2)   # Prints the string two times
print(my_str + "TEST")  # Prints the concatenated string
